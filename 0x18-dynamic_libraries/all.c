#include <stdio.h>

/**
 * mod - mod of two numbers
 * @x: first number
 * @y: second number
 * Return: modulus
 */

int mod(int x, int y)
{
	return (x % y);
}

/**
 * add - add two numbers
 * @x: first number
 * @y: second number
 * Return:sum
 */

int add(int x, int y)
{
	return (x + y);
}

/**
 * sub - subtract two numbers
 * @x: first number
 * @y: second number
 * Return: difference
 */

int sub(int x, int y)
{
	return (x - y);
}

/**
 * div - divide two numbers
 * @x: first number
 * @y: second number
 * Return: Division result
 */

int div(int x, int y)
{
	return (x / y);
}

/**
 * mul - multiply two numbers
 * @x: first number
 * @y: second number
 * Return: multiplication result
 */

int mul(int x, int y)
{
	return (x * y);
}
